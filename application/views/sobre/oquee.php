<h1><b>Metodologia de Ensino</b></h1><br>
<p>  
Para a melhor efici&ecirc;ncia do aprendizado do aluno, cada aula dos cursos possui a seguinte estrutura:</p>
<p>
- <b>Leitura Anterior &agrave; Aula - </b> &Eacute; enviado ao Aluno, antes da realiza&ccedil;&atilde;o da aula indica&ccedil;&otilde;es de manuais e artigos que versam sobre o conte&uacute;do da aula que ser&aacute; dada. Desta forma, o aluno adquire conhecimento pr&eacute;vio, e consolidar&aacute; o conhecimento que j&aacute; come&ccedil;ou a adquirir antes da aula.<br>

- <b>Aula Web - </b> As aulas s&atilde;o dadas por instrutores e consultores da Rede Industrial, que j&aacute; utilizaram os conceitos ensinados em diversas empresas de diversos ramos em todo o Brasil.<br>

- <b>Teste interativo ap&oacute;s a aula -</b> Para consolidar o conhecimento adquirido na aula, &eacute; dado um teste ao aluno de forma que se verifique o seu conhecimento sobre o assunto estudado.<br>

- <b>Discuss&atilde;o no F&oacute;rum -</b> O Aluno dever&aacute; participar em um f&oacute;rum de discuss&atilde;o, que tratar&aacute; sobre o assunto aprendido na aula. Sendo assim, ele ir&aacute; expor suas d&uacute;vidas e experi&ecirc;ncias sobre o assunto, recebendo a contribui&ccedil;&atilde;o dos instrutores e outros alunos de sua turma.</p>
<br>

<p>Todos os alunos receber&atilde;o gratuitamente:<br> 
<b>></b> Acesso &agrave; Biblioteca T&eacute;cnica Virtual<br>
<b>></b> Acesso &agrave; Central de V&iacute;deos T&eacute;cnicos<br>
<b>></b> Acesso &agrave; Rede Social de planejadores de manuten&ccedil;&atilde;o e engenheiros<br>
<b>></b> Acesso &agrave; Central para testes de conhecimento e checklists<br>
<b>></b> Acesso &agrave; Central do aluno PCM e atendimento personalizado<br>
<b>></b> Certificado de Conclus&atilde;o Eletr&ocirc;nico (aprovados)<br>
<b>></b> Vers&atilde;o Full Gratuita do software SIGMA</p>
<br>
<p>Ap&oacute;s a s&eacute;rie de aulas do Curso, ser&aacute; realizado uma prova em tempo real, onde o aluno dever&aacute; responder a quest&otilde;es discursivas sobre o conte&uacute;do das aulas apresentadas.</p>
<br>
<p>Havendo o aproveitamento de 80 % em rela&ccedil;&atilde;o &agrave; nota final, e a presen&ccedil;a m&iacute;nima de 80 % em todas as aulas, o aluno receber&aacute; o certificado de conclus&atilde;o referente ao curso realizado pelo mesmo.</p>
<br>
<p>Possuindo todos estes recursos e atividades, os conceitos b&aacute;sicos necess&aacute;rios para a execu&ccedil;&atilde;o do Planejamento e Controle da manuten&ccedil;&atilde;o ser&atilde;o adquiridos pelo profissional.</p>
