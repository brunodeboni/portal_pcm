<p>O <b>CPCM - Centro de Forma&ccedil;&atilde;o para o Planejamento e Controle da Manuten&ccedil;&atilde;o</b> &eacute; mais uma iniciativa desenvolvida pela <b>Rede Industrial</b> que visa oferecer treinamento aos profissionais envolvidos na &aacute;rea de manuten&ccedil;&atilde;o, utilizando as ferramentas t&eacute;cnicas desenvolvidas pela Rede Industrial.</p>
<br>
<p>Atrav&eacute;s dos <b>Cursos Gratuitos</b> oferecidos, que totalizam 46 horas-aula, o profissional usu&aacute;rio do portal ser&aacute; preparado para iniciar seu trabalho no controle da manuten&ccedil;&atilde;o, utilizando ferramentas de ensino atualizadas e pr&aacute;ticas, unindo os conceitos aprendidos ao <b>Software Sigma</b>.</p>
<br>
<p>Atualmente o CPCM oferece 5 diferentes Cursos Online (EAD) de curta dura&ccedil;&atilde;o, de forma que os alunos podem aprofundar os seus conhecimentos sobre os assuntos ap&oacute;s a participa&ccedil;&atilde;o no curso.</p>

